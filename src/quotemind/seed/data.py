"""Static demo seeds: the 60-SKU catalog and 8 customers (Appendix A.1 / A.2).

Why 60 and not 8: with a tiny catalog, "top-1 SKU accuracy" measures nothing - almost any retrieval
would land on the right row. The eval only says something once the matcher has to discriminate
between near-neighbours (Latitude 5450 i5 vs i7 vs 5440; P2422H vs P2423D vs P2723DE), so the
catalog is deliberately built with confusable families.

All identifiers, prices and MSTs are SAMPLE demo data (Appendix A.6). Margins are set so most SKUs
clear the 5% floor and a handful do not - the critic needs something to catch.
"""

from __future__ import annotations

from quotemind.models import BilingualText, CatalogProduct, CustomerProfile
from quotemind.models.common import Category, Currency, Language, StockStatus, Tier


def _product(
    sku: str,
    brand: str,
    category: Category,
    vi: str,
    en: str,
    specs: dict[str, str],
    unit: str,
    list_price: int,
    dealer_price: int,
    cost_price: int,
    *,
    stock: StockStatus = StockStatus.IN_STOCK,
    lead_time_days: int = 7,
    warranty_months: int = 12,
    vat_rate: int = 8,
) -> CatalogProduct:
    return CatalogProduct(
        sku=sku,
        brand=brand,
        category=category,
        name=BilingualText(vi=vi, en=en),
        specs=specs,
        unit=unit,
        list_price_vnd=list_price,
        dealer_price_vnd=dealer_price,
        cost_price_vnd=cost_price,
        vat_rate=vat_rate,
        stock_status=stock,
        lead_time_days=lead_time_days,
        warranty_months=warranty_months,
    )


